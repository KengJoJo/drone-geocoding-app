        st.success(f"ü§ñ AI ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏Ñ‡∏≥‡∏ú‡∏¥‡∏î‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à: '{user_input}' ‡∏ñ‡∏π‡∏Å‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÄ‡∏õ‡πá‡∏ô '{matched_name}' (‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô: {score}%)")
    
